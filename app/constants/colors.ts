/**
 * ルーレットのセグメントで使用するカラーパレット
 * 20色以上のカラーパレットを提供
 */
export const COLORS = [
  '#FF9999', // サーモンピンク
  '#99CCFF', // スカイブルー
  '#99FF99', // ライトグリーン
  '#FFB366', // ライトオレンジ
  '#FF99CC', // ピンク
  '#9999FF', // ラベンダー
  '#FFFF99', // ライトイエロー
  '#FF99FF', // マゼンタ
  '#99FFFF', // アクアブルー
  '#FFB399', // コーラル
  '#CC99FF', // ライトパープル
  '#FF9966', // ピーチ
  '#99FF66', // ライムグリーン
  '#66B2FF', // ドッジブルー
  '#FF66B2', // ホットピンク
  '#B2FF66', // イエローグリーン
  '#66FFB2', // ミントグリーン
  '#B266FF', // パープル
  '#FFB266', // アプリコット
  '#66FFE6', // ターコイズ
  '#FF6666', // ライトレッド
  '#66FF66', // ブライトグリーン
  '#6666FF', // ブライトブルー
  '#FFE666', // バナナイエロー
] as const;

/**
 * カラーパレットから色を取得する
 * @param index インデックス
 * @returns 色のHEX値
 */
export function getColor(index: number): string {
  return COLORS[index % COLORS.length];
} 